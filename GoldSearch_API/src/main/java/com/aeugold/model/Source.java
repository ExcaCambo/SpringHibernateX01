package com.aeugold.model;

public class Source {
	private int src_id;
	private String src_name;
	private String src_link;
	private Image src_img;
	
	public int getSrc_id() {
		return src_id;
	}
	public void setSrc_id(int src_id) {
		this.src_id = src_id;
	}
	public String getSrc_name() {
		return src_name;
	}
	public void setSrc_name(String src_name) {
		this.src_name = src_name;
	}
	public String getSrc_link() {
		return src_link;
	}
	public void setSrc_link(String src_link) {
		this.src_link = src_link;
	}
	public Image getSrc_img() {
		return src_img;
	}
	public void setSrc_img(Image src_img) {
		this.src_img = src_img;
	}
	
	
}
